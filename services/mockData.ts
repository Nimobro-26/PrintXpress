// Generate OTP (6 digits)
export const generateOTP = (): string => {
  return Math.floor(100000 + Math.random() * 900000).toString();
};

// Calculate print cost
export const calculatePrintCost = (
  pages: number,
  colorMode: 'bw' | 'color',
  copies: number,
  highQuality: boolean
): number => {
  const baseRate = colorMode === 'bw' ? 0.10 : 0.25;
  const qualityMultiplier = highQuality ? 1.5 : 1.0;
  return pages * copies * baseRate * qualityMultiplier;
};
